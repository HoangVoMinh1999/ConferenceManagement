    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        Place temp = new Place();
        temp.setName(namePlaceTextField.getText());
        temp.setCapacity(Integer.parseInt(capacityTextField.getText()));
        temp.setAddress(addressTextField.getText());
        if (plDAO.save(temp)) {
            JOptionPane.showMessageDialog(null, "Add place successfully !!!");
            try {
                Thread.sleep(1000);
                JFrameIndexAdmin j = new JFrameIndexAdmin();
                j.setVisible(true);
                this.dispose();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Add place unsuccessfully !!!");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

